// server/storage.ts
import { eq } from "drizzle-orm";
var DatabaseStorage = class {
  async getUser(id) {
    const [user] = await db.select().from(users).where(eq(users.id, id));
    return user || void 0;
  }
  async getUserByUsername(username) {
    const [user] = await db.select().from(users).where(eq(users.username, username));
    return user || void 0;
  }
  async createUser(insertUser) {
    const [user] = await db.insert(users).values(insertUser).returning();
    return user;
  }
  async createContact(insertContact) {
    const [contact] = await db.insert(contacts).values(insertContact).returning();
    return contact;
  }
  async getContacts() {
    return await db.select().from(contacts).orderBy(contacts.createdAt);
  }
};
var storage = new DatabaseStorage();

// server/routes.ts
import path from "path";
import fs from "fs";
async function registerRoutes(app2) {
  if (process.env.NODE_ENV === "production") {
    app2.get("/blogs", (req, res) => {
      const blogsPath = path.resolve(import.meta.dirname, "public", "blogs.html");
      if (fs.existsSync(blogsPath)) {
        res.sendFile(blogsPath);
      } else {
        res.redirect("/");
      }
    });
  }
  app2.post("/api/contacts", async (req, res) => {
    try {
      const contactData = insertContactSchema.parse(req.body);
      const contact = await storage.createContact(contactData);
      console.log(`\u{1F4E7} New contact submission from ${contact.name} (${contact.email})`);
      res.json({
        success: true,
        message: "Contactbericht succesvol ontvangen",
        contact: {
          id: contact.id,
          name: contact.name,
          createdAt: contact.createdAt
        }
      });
    } catch (error) {
      console.error("Error creating contact:", error);
      res.status(400).json({
        success: false,
        message: "Er ging iets mis bij het versturen van je bericht"
      });
    }
  });
  app2.get("/api/contacts", async (req, res) => {
    try {
      const contacts2 = await storage.getContacts();
      res.json(contacts2);
    } catch (error) {
      console.error("Error fetching contacts:", error);
      res.status(500).json({
        success: false,
        message: "Er ging iets mis bij het ophalen van contactberichten"
      });
    }
  });
  const httpServer = createServer(app2);
  return httpServer;
}

